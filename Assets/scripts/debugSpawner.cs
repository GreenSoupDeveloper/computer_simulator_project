using UnityEngine;
using UnityEngine.UI;
using TMPro;
public class debugSpawner : MonoBehaviour
{
    public GameObject casePrefab;
    public GameObject cpuPrefab;
    public GameObject gpuPrefab;
    public GameObject moboPrefab;
    public GameObject powerSupplyPrefab;
    public GameObject hddPrefab;
    public GameObject ramPrefab;
    public GameObject cpuFanPrefab;
    public Transform target;

    void Update(){
        if(Input.GetKeyDown(KeyCode.F1)){
            Instantiate(casePrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F2)){
            Instantiate(cpuPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F3)){
            Instantiate(gpuPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F4)){
            Instantiate(moboPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F5)){
            Instantiate(powerSupplyPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F6)){
            Instantiate(hddPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F7)){
            Instantiate(ramPrefab, target.transform.position, Quaternion.identity);
        }
        if(Input.GetKeyDown(KeyCode.F8)){
            Instantiate(cpuFanPrefab, target.transform.position, Quaternion.identity);
        }

    }
}